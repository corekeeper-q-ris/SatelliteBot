        /// <summary>
        /// Получить все планеты.
        /// </summary>
        public static List<string> GetPlanets()
        {
            return Satellites.Select(s => s.PlanetHost).Distinct().ToList();
        }

        /// <summary>
        /// Получить спутники планеты.
        /// </summary>
        public static List<Satellite> GetSatellitesByPlanet(string planet)
        {
            return Satellites
                .Where(s => s.PlanetHost.Equals(planet, System.StringComparison.OrdinalIgnoreCase))
                .ToList();
        }

        /// <summary>
        /// Получить спутник по названию.
        /// </summary>
        public static Satellite GetSatelliteByName(string name)
        {
            return Satellites
                .FirstOrDefault(s => s.Name.Equals(name, System.StringComparison.OrdinalIgnoreCase));
        }

        /// <summary>
        /// Получить информацию о всех спутниках.
        /// </summary>
        public static string GetAllSatellitesInfo()
        {
            var result = "Спутники планет Солнечной системы:\n\n";
            foreach (var planet in GetPlanets())
            {
                result += $"*{planet}:*\n";
                var planetSatellites = GetSatellitesByPlanet(planet);
                foreach (var satellite in planetSatellites)
                {
                    result += $"  - {satellite.Name}\n";
                }
                result += "\n";
            }
            return result;
        }
